function love.conf(t)
    t.title = "Pong"
    -- the title of the window of the game is in (string)
    t.version = "11.4"
    -- The love version this game was made for (string)
    t.console = true
    -- attach a console (boolean, windows only)
    t.window.width = 1280
    -- the window width (number)
    t.window.height = 720
    -- the window height (number)
end